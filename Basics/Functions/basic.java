public class basic {
    static void greet(String name) {  // Function with parameter 'name'
        System.out.println("Good Morning, " + name + "!!");  // Printing a greeting
    }

    public static void main(String[] args) {
        greet("Sumit");  // Calling greet function with argument "Sumit"
        greet("Saurabh");  // Calling greet function with argument "Saurabh"
    }
}
